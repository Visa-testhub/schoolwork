#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 10 && n >= 0)
		ft_putchar(48 + n);
	else if (n == -2147483648)
		write(1, "-2147483648", 11);
	else if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else if (n < 0 && n != -2147483648)
	{
		ft_putchar('-');
		ft_putnbr(-n);
	}
}
