int	ft_atoi(const char *s)
{
	int	i;
	int	j;
	int	nb;

	i = 0;
	j = 0;
	nb = 0;
	while (s[i] == '\t' || s[i] == '\v' || s[i] == '\r' || s[i] == '\f' ||\
			s[i] == '\n' || s[i] == ' ')
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		j = i;
		i++;
	}
	while (s[i] != '\0' && s[i] >= '0' && s[i] <= '9')
	{
		nb = nb * 10 + (s[i] - 48);
		i++;
	}
	i = 0;
	if (s[j] == '-')
		nb = nb * -1;
	return (nb);
}
