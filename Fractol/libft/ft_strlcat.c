#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t i;
	size_t j;
	size_t k;

	i = ft_strlen(dst);
	k = i;
	j = 0;
	if (dstsize <= k)
	{
		return (dstsize + ft_strlen(src));
	}
	while (src[j] && j < dstsize - k - 1)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (k + ft_strlen(src));
}
